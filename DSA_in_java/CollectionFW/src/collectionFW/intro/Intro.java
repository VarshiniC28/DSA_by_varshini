public class Intro {

	public static void main(String[] args) {

		//Collection interface
		//List interface implements collection interface.
		//ArrayList and LinkedList class extends to list interface
		//Vector legacy class also implements List interface
		//Stack class extends Vector class
		
		//Queue interface extends to collection interface
		//PriorityQueue class implements Queue interface
		//Dequeue interface also extends to Queue interface
		//ArrayDequeue implements the Dequeue interface
		
		//Set interface extends collection interface
		//sorted set extends to set interface
		//NavigableSet extends to SortedSet
		//TreeSet implements NavigableSet
		//-- HashSet class implements Set Interface
		//LinkedHashSet class extends HashSet class
		
		//Map interface is separate from collection interface
		//AbstractMap class implements Map interface
		//HashMap class and enum classes extends AbstractMap class
		//LinkedHashMap class extends HashMap class
		//SortedMap interface extends Map interface
		//Navigable Interface extends SortedMap Interface
		//TreeMap class implements navigable interface
	}

}
